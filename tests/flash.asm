; ===========================================================================
; flash.asm — Flash ROM / BIOS shadow read tests (4 assertions)
; ===========================================================================
%include "harness.inc"

org 0x1000

    ; ------------------------------------------------------------------
    ; 1. Read from BIOS shadow area (0xFF000000) — should be 0xFF (empty)
    ; ------------------------------------------------------------------
    mov eax, [0xFF000000]
    ASSERT_EQ eax, 0xFFFFFFFF

    ; ------------------------------------------------------------------
    ; 2. Read from Flash base (0xF0000000) — same content, aliased
    ; ------------------------------------------------------------------
    mov eax, [0xF0000000]
    ASSERT_EQ eax, 0xFFFFFFFF

    ; ------------------------------------------------------------------
    ; 3. Byte read from BIOS area
    ; ------------------------------------------------------------------
    movzx eax, byte [0xFF000010]
    ASSERT_EQ eax, 0xFF

    ; ------------------------------------------------------------------
    ; 4. 16-bit read from flash area
    ; ------------------------------------------------------------------
    movzx eax, word [0xF0000020]
    ASSERT_EQ eax, 0xFFFF

    PASS
